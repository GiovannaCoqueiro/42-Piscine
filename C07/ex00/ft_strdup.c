#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*dest;
	int		count;

	count = 0;
	dest = malloc(sizeof(src) + 1);
	while (src[count] != '\0')
	{
		dest[count] = src[count];
		count++;
	}
	dest[count] = '\0';
	return (dest);
}
